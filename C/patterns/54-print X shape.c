#include <stdio.h>



/*********************************************************************/
/*********************************************************************/
/************          A program to print x shape         ************/         
/*************         			pattern					  ************/ 
/*********************************************************************/
/*********************************************************************/
int main()
{
    int rows, stars;
    int count=12;

    for(rows=1; rows<=count; rows++)
    {
        for(stars=1; stars<=count; stars++)
        {
            if(stars==rows || (stars==count - rows +1))
            {
                printf("*");
            }
            else
            {
                printf(" ");
            }
        }

        printf("\n");
    }

    return 0;
}